import Foundation

/// Defaults for importing into a **Publish** site.
public enum PublishDefaults {
  /// Default directory name for markdown content.
  public static let contentDirectoryName = "Content"
  /// Default directory name for root resources.
  public static let resourcesDirectoryName = "Resources"
  /// Default directory name for imported **WordPress** resources.
  public static let wpAssetsRelativePath = "media/wp-assets"
}
